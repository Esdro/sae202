  <h3>Concept</h3>
  <p>
    1969, en pleine révolution culturelle, le mythique club <strong>Mainstay</strong> accueille une soirée disco privée 
    avec l’élite artistique de l’époque. Strass, groove et secrets sont au rendez-vous. 
    Mais à minuit, la fête tourne au drame : une icône est retrouvée morte sur la piste.
    Qui l’a tuée ? Pourquoi ? Et que cachent les autres invités ?
  </p>
</div>

