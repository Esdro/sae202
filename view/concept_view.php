<div class="contenu">
  <h2 id="regles">Règles du jeu</h2>
  <p>
    Bienvenue au <strong>Mainstay Club</strong>, où groove, secrets et meurtres s’entremêlent sous les boules à facettes…
  </p>

  <!-- Objectif du jeu -->
  <h1>Objectif du jeu</h1>
  <p>
    À minuit, une célébrité est retrouvée morte sur la piste. L’un d’entre vous est le meurtrier.
  </p>
  <ul>
    <li><strong>Si vous êtes innocent :</strong> découvrez l'identité du meurtrier.</li>
    <li><strong>Si vous êtes le meurtrier :</strong> brouillez les pistes pour ne pas être démasqué.</li>
  </ul>

  <!-- Description des personnages -->
  <h1>Personnages & secrets</h1>
  <p>Chaque joueur incarne un personnage avec :</p>
  <ul>
    <li>Une identité (profession, style, traits)</li>
    <li>Des objectifs personnels</li>
    <li>Un ou plusieurs secrets à protéger ou à exploiter</li>
    <li>Des relations avec d'autres personnages</li>
  </ul>
  <p>
    Tous les secrets ne concernent pas le meurtre… mais ils peuvent semer le doute !
  </p>


