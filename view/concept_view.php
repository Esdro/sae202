<div class="intro">
  <h3>Concept</h3>
  <p>
    1969, en pleine révolution culturelle, le mythique club <strong>Mainstay</strong> accueille une soirée disco privée 
    avec l’élite artistique de l’époque. Strass, groove et secrets sont au rendez-vous. 
    Mais à minuit, la fête tourne au drame : une icône est retrouvée morte sur la piste.
    Qui l’a tuée ? Pourquoi ? Et que cachent les autres invités ?
  </p>
</div>

<!-- Section principale du contenu -->
<div class="contenu">
  <h2 id="regles">Règles du jeu</h2>
  <p>
    Bienvenue au <strong>Mainstay Club</strong>, où groove, secrets et meurtres s’entremêlent sous les boules à facettes…
  </p>

  <!-- Objectif du jeu -->
  <h1>Objectif du jeu</h1>
  <p>
    À minuit, une célébrité est retrouvée morte sur la piste. L’un d’entre vous est le meurtrier.
  </p>
  <ul>
    <li><strong>Si vous êtes innocent :</strong> découvrez l'identité du meurtrier.</li>
    <li><strong>Si vous êtes le meurtrier :</strong> brouillez les pistes pour ne pas être démasqué.</li>
  </ul>

  <!-- Description des personnages -->
  <h1>Personnages & secrets</h1>
  <p>Chaque joueur incarne un personnage avec :</p>
  <ul>
    <li>Une identité (profession, style, traits)</li>
    <li>Des objectifs personnels</li>
    <li>Un ou plusieurs secrets à protéger ou à exploiter</li>
    <li>Des relations avec d'autres personnages</li>
  </ul>
  <p>
    Tous les secrets ne concernent pas le meurtre… mais ils peuvent semer le doute !
  </p>

  <!-- Déroulement du jeu -->
  <h1>Déroulement du jeu</h1>
  <ul>
    <li>
      <strong>1. Accueil & brief (15-20 min)</strong><br>
      Remise des fiches personnages, introduction de l’histoire, mise en ambiance
    </li>
    <li>
      <strong>2. Immersion libre & enquête (1h à 1h30)</strong><br>
      Interactions, recherches d’indices, discussions, soupçons…
    </li>
    <li>
      <strong>3. Révélation finale (30 min)</strong><br>
      Bulletins d’accusation, dévoilement du coupable et résolution de l’affaire
    </li>
  </ul>

  <!-- Règles de comportement -->
  <h1>Règles de comportement</h1>
  <ul>
    <li>Restez dans votre personnage</li>
    <li>Ne montrez jamais votre fiche personnage</li>
    <li>Respectez le cadre 1969 (pas de références modernes)</li>
    <li>Pas de violence réelle – le jeu reste bon enfant</li>
  </ul>

  <!-- Conseils de jeu -->
  <h1>Conseils pour bien jouer</h1>
  <ul>
    <li>Écoutez bien les autres : tout peut être un indice</li>
    <li>Soyez expressif : plus vous jouez le jeu, plus c’est fun</li>
    <li>N’ayez pas peur du drama : mensonges, rebondissements, révélations sont le cœur du jeu</li>
    <li>Poursuivez vos objectifs personnels tout en enquêtant</li>
  </ul>

  <p>
    <strong>Souviens-toi :</strong> au Mainstay Club, <em>personne n’est innocent… vraiment.</em>
  </p>
</div>

